package com.example.filmamora.Objet;

public class Realisateur {
    private int id;
    private String name;

    public Realisateur(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
